package com.rx_1;

public class Student {

	
	private String name;
	private int age;
	private int roll;
	
	public void Setname(String name)
	{
		this.name = name;
	}
	
	public String Getname()
	{
		return this.name;
	}
	
	public void Setage(int age)
	{
		this.age = age;
	}
	
	public int Getage()
	{
		return this.age;
	}
	
	public void Setroll(int roll)
	{
		this.roll = roll;
	}
	
	public int Getroll()
	{
		return this.roll;
	}
	
}
